class incr
{
public static void main(String[] args)
{
          int result = +10; 
          System.out.println(result);
          result--;  
          System.out.println(result);
          result++; 
          System.out.println(result);
          --result;  
          System.out.println(result);
          ++result; 
          System.out.println(result);
          result = -result; 
          System.out.println(result);
          boolean success = false;
          System.out.println(success); 
          System.out.println(!success); 
     }
}
