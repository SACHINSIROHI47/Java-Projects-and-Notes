public class alldatatype
{
public static void main(String[] ar)
{
//Numeric data type
byte bt=56;
System.out.println(bt);
short sh=4567;
System.out.println(sh);
int num=78945895;
System.out.println(num);
long mob=789458798578945L;
System.out.println(mob);

//Decimal Data type
float price=4578.78f;
System.out.println(price);
double amt=7878797.89789787d;
System.out.println(amt);

//String and character Data type
char ch='g';
System.out.println(ch);
String address="Western kutchery road";
System.out.println(address);

//Boolean Data Type
boolean pass=true;
boolean fail=false;
System.out.println(pass+" "+fail);
}
}